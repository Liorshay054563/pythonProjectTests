import math

def add(a,b):
    return a + b

def minus(a,b):
    return a - b

def multiply(a,b):
    return a * b

def divide(a,b):
    return a / b

def power(a,b):
    return a**b

def sqrt(a):
    return math.sqrt(a)

def factorial(a):
    # if a < 0:
    #     raise ValueError()
    return math.factorial(a)
